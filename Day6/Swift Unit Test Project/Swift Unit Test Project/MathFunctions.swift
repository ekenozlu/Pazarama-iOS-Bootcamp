import Foundation

class MathFunctions {
    func add(x: Int, y: Int) -> Int {
        return x + y
    }
    
    func multiply(x: Int, y: Int) -> Int {
        return x * y
    }
    
    func substruct(x: Int, y: Int) -> Int {
        return x / y
    }
}
